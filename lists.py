#append()
l=[1,2,3,4,5]
l.append(3)
print(l)

#insert()
a=[1,2,3,4,5]
a.insert(0,12)
print(a)

#remove()
b=[1,2,3,4,5]
b.remove(4)
print(b)

#sort()
c=[1,2,34,4,5,5]
c.sort()
print(c)

#pop()
d=[1,2,3,4,5]
d=d.pop(1)
print(d)

#index()
e=[1,2,3,4,5]
e=e.index(3)
print(e)

#count
f=[1,2,3,4,5,3]
f=f.count(3)
print(f)

#reverse
t=[1,2,3,4,5]
t.reverse()
print(t)



